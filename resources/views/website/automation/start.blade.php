  <p>Alzheimer’s Disease - All You Need to Know Guide (2021)</p>
  Before we start, I would like to point out that the progression of Alzheimer's varies from person to person.
  <p>How Alzheimer’s develops in your case can not be predicted by this guide.</p>
  <p>There are specific tests in order to get a good estimate. I will outline such tests later in Section 5.</p>
  <p>If you just started your research then read the entire guide from start to finish. Otherwise, feel free to skip the basics and jump right into a topic of your interest:</p>
  <p>I put this guide together for all family members, who take care of an individual diagnosed with dementia and/or Alzheimer's disease. <br></p>
  <p>From personal experience with my grandparents, I sympathize with everybody, who has to take care of their loved ones. Not only is it very demanding, but it is an enormous responsibility.</p>
  <p>Throughout this article, I am referring to “patients” and “people suffering from Alzheimer’s / Dementia”. <br></p>
  <p>Individuals might be considered patients, but they have the right to get the best possible care.</p>
  <h2>What is Alzheimer's Disease?</h2>
  <p><b>Alzheimer's disease is defined as a progressive form of dementia. Suggesting that it is a degenerative disease, which worsens over time. And just like coronary artery disease is a type of heart disease, Alzheimer's is a type of brain disease.</b></p>
  <p>The most intriguing characteristic for me is, how the brain unnoticeably changes 20 years until any warning signs emerge. But, brain changes can also occur more than 20 years in advance, too.<br></p>
  <p>Many years pass by, until a patient experiences any sort of symptoms. Including problems with the sense of smell or memory loss.</p>
  <p>Symptoms become noticeable, after neurons located in brain parts responsible for functions like memory, thinking, or learning are already being damaged.</p>
  <p>Ultimately, nerve cells that were entirely unaffected by Alzheimer's begin to be injured, too. <br></p>
  <p>Such nerve cells can be responsible for our basic body functions. <br></p>
  <p>Examples could be swallowing or breathing.</p>
  <p>As you can imagine by now, an individual becomes sooner or later bed-bound.</p>
  <p>Ultimately, the patient requires 24/7 Professional Care. <br></p>
  <p>And eventually, Alzheimer's is fatal for a person suffering from it.</p>
  <h2>Alzheimer’s or Dementia - Is there a Difference?</h2>
  <p><b>“Dementia” and “Alzheimer’s Disease” are not the same: Dementia is understood as a pattern of symptoms. Various causes can lead to developing dementia. Alzheimer’s disease is known to be one of the most common causes of dementia. </b><br></p>
  <p>Dementia is characterized by a decrease in numerous cognitive functions. <br></p>
  <p>But, dementia is not a true memory disorder.</p>
  <p><i><b>Symptoms include difficulties with memory, problem-solving, and language.</b></i></p>
  <p>My grandparents suffered both from Dementia. And on many occasions, I observed that both found it increasingly difficult to memorize new information. <br></p>
  <p>The same accounted for focusing on a thought or object.</p>
  <p>While my grandmother was fine with knowing time, dates, and places, my grandpa seemed to somehow struggle.</p>
  <p>Dementia patients have limited cognitive abilities to the extent that familiar everyday activities can no longer be carried out as before.</p>
  <p>Changes in social behavior, impulse control, and drive can occur, too. Occasionally, these changes can surpass other symptoms.</p>
  <p>Finally, emotional states such as depression, anxiety, or restlessness can further reduce cognitive abilities.</p>
  <p>Symptoms of Dementia can vary from person to person. <br></p>
  <h2>Brain Changes Associated with Alzheimer’s</h2>
  <p><b>As you might already know, around 100 billion neurons are located in a healthy adult brain. They contain branching extensions, which create connections with surrounding neurons. These connections, known as synapses, allow information to flow. This happens when a neuron releases tiny bursts of chemicals that can be detected by another neuron.</b></p>
  <p>Synapses create a cellular basis i. e. for memories, thoughts, emotions, sensations, and movements. <br></p>
  <p>They do so by allowing signals to travel quickly through the neuronal circuits of the brain.</p>
  <p><i><b>Did you know? Approximately 100 trillion synapses can be found in the human brain.</b></i></p>
  <p>As already hinted, changes associated with Alzheimer’s have something to do with the neurons.</p>
  <p>So, let’s figure out what:</p>
  <h2>What brain changes are associated with Alzheimer’s?</h2>
  <p><b>The two major brain changes associated with Alzheimer’s are: (1) The accumulation of a protein called “tau” inside of the neurons. And (2) the increase of beta-amyloid plaques outside of the neurons. Both changes can be identified with an autopsy, which usually happens post mortem.</b></p>
  The brain changes due to Alzheimer’s lead to a process known as neurodegeneration.
  <p>Neurodegeneration (damage and death of neurons) starts with the accumulations of plaques and smaller beta-amyloids called oligomers.</p>
  <p>Both of them interfere with neuron-to-neuron communication.</p>
  <p>Most essential nutrients and molecules needed inside the neuron are blocked by the tau tangles. This lack of nutrients damages the neuron.</p>
  <p>Ultimately, the previously healthy neurons die.</p>
  <p>To this date, the sequence of events happening during the progression of Alzheimer’s is still unclear. <br></p>
  <p>It is speculated that beta-amyloid arises before abnormal tau.</p>
  <p>The accumulation of tau and plaques are not the only changes linked to Alzheimer’s.</p>
  <p>For instance, inflammation and atrophy can be associated with the disease, too.</p>
  <p>The immune system cells are believed to be activated once toxic beta-amyloid and tau proteins are present. <br></p>
  <p>Immune cells occur in the microglia, which are located throughout the brain and spinal cord.</p>
  <p><i><b>Microglia are responsible for clearing the debris from dead and dying cells and toxic proteins.</b></i></p>
  <p>Chronic inflammation may set in after microglia can not keep up with their assignment.</p>
  <p><i><b>Chronic inflammation is characterized by the simultaneous destruction and repair of the tissue from the inflammatory process.</b></i></p>
  <p>With this description in mind, I think we can move on to the next brain change related to Alzheimer’s. It is known as Atrophy.</p>
  <p>Atrophy is the progressive degeneration or shrinkage of muscle and nerve tissue. <br></p>
  <p>Like the previous changes, it occurs in the brain, too. <br></p>
  <p>It is directly associated with the effects of Alzheimer’s.</p>
  <p>With all those Alzheimer-related changes in place, the normal cognitive function is further compromised due to the brain’s decreased ability to metabolize glucose.</p>
  <p>I would like to underline that the brain depends heavily on its ability to metabolize glucose.</p>
  <p><i><b>Like a car needs petrol to drive, the brain needs glucose to work smoothly. </b></i><br></p>
  <p>Glucose is the brain’s main fuel, so to speak.</p>
  <h2>The Alzheimer’s Continuum and Symptoms</h2>
  <p><b>You already know Alzheimer’s is a progressive form of dementia. The progression starts with unnoticeable brain changes and develops into changes that cause memory problems. Even physical disabilities can aris</b>e.</p>
  <p>And this is called Alzheimer’s disease continuum.</p>
  <p><u><b>In this chapter, I am going to outline the continuum into three broad phases:</b></u></p>
  <p>(1) Preclinical Alzheimer’s disease, (2) Mild Cognitive Impairment “MCI” due to Alzheimer’s disease, and (3) Dementia due to Alzheimer’s disease.</p>
  <p>The last phase can be divided into three supplementary stages: (3.1) Mild Dementia due to Alzheimer’s, (3.2) Moderate Dementia due to Alzheimer’s, and (3.3) Severe Dementia due to Alzheimer’s.</p>
  <p>All stages characterize the severity to which symptoms have an impact on the individual’s ability to carry out everyday activities.</p>
  <p>I would like to state, though, the continuum starts with preclinical Alzheimer’s and ends with severe Alzheimer’s dementia, it is not established how long one spends in each stage.</p>
  <p>According to <b>Vermunt et. al (2019), </b>the length is influenced by factors like age, genetics, and gender.</p>
  <p>Alzheimer’s Disease (AD) Continuum:</p>
  <p><b>1. Preclinical AD - </b>No Symptoms</p>
  <p><b>2. MCI* due to AD -</b> Very mild symptoms that do not interfere with everyday activities</p>
  <p><b>3. Dementia due to AD:</b>&nbsp; <i>(3.1.) Mild</i><b> -</b> Symptoms interfere with some everyday activities, <i>(3.2.) Moderate </i>- Symptoms interfere with many everyday activities, <i>(3.3.) Severe -</i> Symptoms interfere with most everyday activities.</p>
  <p>MCI* = Mild Cognitive Impairment.</p>
  <p>As you can notice, symptoms interfere more and more with the patient's independence. <br></p>
  <p>The more the disease progresses, the harder the daily task becomes.</p>
  <p>At this point, I am going to describe the individual stages. Facts and figures will be explained step by step.</p>
  <h2>1. Preclinical Alzheimer’s Disease</h2>
  <p><b>Individuals in this phase have measurable brain changes. For example, abnormal levels of beta-amyloid, which can be indicated with the help of positron emission tomography (PET) scans.</b></p>
  <p>More ways to indicate brain changes include analysis of cerebrospinal fluid (CSF) or scanning for decreased metabolism of glucose (analyzed by PET scans).</p>
  <p>In the preclinical Alzheimer's Disease phase, symptoms (i.e. trouble with memory) are not yet existing.</p>
  <p><i><b>Surprisingly, even though brain changes happen, the brain is prepared to compensate for them</b></i>.</p>
  <p>I find this point very fascinating because individuals are still able to function normally in this phase.</p>
  <p>So far, only researchers have the expertise and tools required to identify early brain changes associated with Alzheimer’s disease. <br></p>
  <p>Thus, it is a long way to go, until hospitals and physicians can provide the procedures.</p>
  <p>Another interesting point to talk about:</p>
  <p><b><i>Not all people with evidence of Alzheimer-related brain changes necessarily develop symptoms of MCI or dementia due to Alzheimer’s.</i></b></p>
  <p>Personally, I find this fact very outstanding and interesting.</p>
  <p>There are even cases, in which people have an accumulation of beta-amyloid plaques and they never showed any symptoms (i. e. memory or thinking problems).</p>
  <p>Such cases are usually discovered after death.</p>
  <h2>2. MCI Due to Alzheimer’s disease</h2>
  <p><b>In this phase, individuals show evidence of Alzheimer’s brain changes. Evidence may be abnormal levels of beta-amyloid. Individuals diagnosed with MCI usually have modest problems with thinking and memory.</b></p>
  <p>MCI does not interfere with one’s ability to carry out everyday activities. <br></p>
  <p>And the cognitive changes are only noticeable to close friends and family members.</p>
  <p>The cognitive changes are not obvious to strangers.</p>
  <p>Unlike in preclinical Alzheimer’s, where one’s brain can still compensate for the brain changes, MCI leads to a rather stronger effect.</p>
  <p>It interferes with one's thinking ability. This effect arises because the brain can no longer compensate for the changes.</p>
  <p>I would like to point out that the damage and death of nerve cells (neurons) are already irreversible. Alzheimer’s disease gains momentum from here on out. <br></p>
  <p>The disease’s progression increases in speed. <br></p>
  <p>However, MCI reverts back to normal cognition for a few individuals. <br></p>
  <p>Others can even remain in MCI without moving to the next stage.</p>
  <p><b>Another point to consider:</b> MCI is sometimes diagnosed by mistake. <br></p>
  <p>For example, a medication could cause cognitive changes, too.</p>
  <p>Then, changes can be simply reversed by changing the medication.</p>
  <p>Research focuses mainly on the identification of people, who are at risk of developing Alzheimer’s or other types of dementia, once diagnosed by MCI.</p>
  <h2>3. Dementia Due to Alzheimer’s Disease</h2>
  <p><b>This phase is characterized by the symptoms that impair one’s ability to function in everyday life. Symptoms include noticeable behavioral, memory, and thinking changes. Alzheimer-related brain changes serve as evidence for entering this phase.</b></p>
  <p>Compared to the previous two phases, individuals experience several symptoms, which continuously change over the upcoming years.</p>
  <p>Symptoms are a direct reflection of how much damage the neurons suffered.</p>
  <p>The time required by dementia in order to advance from mild to moderate to severe is different from patient to patient.</p>
  <p>In the subsequent sections, I talk about the three stages of Alzheimer's dementia. <br></p>
  <p><b>The three stages include 1) Mild, 2) Moderate, and 3) Severe.</b></p>
  <h2>3.1 Mild Alzheimer’s Dementia</h2>
  <p><b>People diagnosed with mild Alzheimer’s dementia are still capable to function in many areas of their everyday life. Assistance from a caregiver is not required. Nonetheless, some activities already require help to stay safe.</b></p>
  <p>One activity I can illustrate based on experience with my grandparents is cutting food during the preparation of a meal. <br></p>
  <p>It can be dangerous to give a knife to an Alzheimer’s patient.</p>
  <p>I would not recommend any caregiver to allow their patient to chop fresh onions with a sharp knife.</p>
  <p>Individuals with mild Alzheimer's dementia may still be capable to work, drive a car or bicycle, or take part in their favorite sport (i. e. jogging, playing golf, or tennis).</p>
  <p>As you can imagine, people in this phase are still very independent.</p>
  <p>But, this sense of independence will change with the next two phases.</p>
  <h2>3.2 Moderate Alzheimer’s Dementia</h2>
  <p><b>In the moderate phase of Alzheimer’s dementia, individuals can no longer perform their daily activities like before. Usually, difficulties in communicating and performing routine tasks arise.</b></p>
  <p>They might become incontinent and start needing help with bathing and dressing.</p>
  <p>This stage does not require around-the-clock care. <br></p>
  <p>Besides, it is often the longest-lasting stage.</p>
  <p>Personality changes and mood changes are common in patients with Moderate Alzheimer’s Dementia.</p>
  <p>Behavioral changes include agitation as well as suspiciousness.</p>
  <p>From personal experience, I can say: This phase is very irritating for family members once the personality changes arise.</p>
  <p>Family members should start making financial plans to prepare for the last stage. <br></p>
  <p><i><b>The earlier financial plans are being made the better the opportunities for good care.</b></i></p>
  <p>I personally can not stress enough this point. Please search for advice from your insurance</p>
  <p>provider and start getting the first offers from nursing homes.</p>
  <p>Just to give you a number: In Germany, a monthly bill for a spot in a decent nursing home is at least 5.000 EUR per month. This does not come close to a spot in a top-notch nursing home.</p>
  <p>Another option is to pay a full-time salary for a nurse/caretaker, which stays at home with the patient.</p>
  <p>This can be cheaper, depending on whether or not your family member has to pay rent or owns the property.</p>
  <p>The salary of a nurse tends to be a bit higher (around 3.500 EUR per month), while a caretaker without a degree of specialization can be slightly less (range between 2.200 - 3.500 EUR per month).</p>
  <p>The nurse/caretaker requires a separate room in order to live with the patient.</p>
  <p>This point is important to remember, in case you don't own an entire home or a two-room apartment. Otherwise, this is not the option to go.</p>
  <p>Seek early advice in order to find the best solution.</p>
  <h2>3.3 Severe Alzheimer’s Dementia</h2>
  <p>In the severe phase of Alzheimer’s dementia, daily living and normal activities require caregiving help. Eventually, 24/7 care is needed. Patients’ physical health is already impacted by the effects of Alzheimer’s. Usually, the individual is bed-bound due to damage in the areas of the brain responsible for movement.</p>
  <p>At this point, patients are extremely vulnerable to different conditions like skin infections, blood clots, and sepsis.</p>
  <p>Body-wide inflammation is triggered by sepsis and leads to organ failure.</p>
  <p>Brain damage in areas controlling the way we swallow makes it more and more difficult to drink and eat.</p>
  <p>Individuals start to swallow food into the windpipe (trachea) instead of the food pipe (esophagus). Commonly resulting in a lung infection caused by food particles accumulating in the lungs.</p>
  <p>This sort of lung infection is known as aspiration pneumonia - one of the contributing causes of death for individuals suffering from Alzheimer’s.</p>
  <h2>Diagnosis of Dementia due to Alzheimer’s Disease</h2>
  <p>There is no standardized test for diagnosing Alzheimer's dementia that can be applied by your physician himself. What happens instead is the use of different tools and procedures. Due to lack of expertise, the physicians receive help from their fellow colleagues including Neuropsychologists, Neurologists, Geriatricians, and geriatric Psychiatrists.</p>
  <p>You can find the most common tests, examinations, and tools used for a Diagnosis of Alzheimer’s dementia in the following table:</p>
  <p><b>Obtaining Medical, Family, and Psychiatric History</b></p>
  <p><b>Obtaining History of Behavioral ChangesObtaining History of Cognitive Changes</b></p>
  <p><b>Interview with Family Members - Additional Input about Changes in Thinking and</b></p>
  <p><b>Behavior</b></p>
  <p><b>Problem-Solving Tests</b></p>
  <p><b>Memory Tests</b></p>
  <p><b>Other Cognitive Tests</b></p>
  <p><b>Physical and Neurological Examinations</b></p>
  <p><b>Blood Tests</b></p>
  <p><b>Brain Imaging - Rule out Tumor and Vitamin Deficiencies</b></p>
  <p><b>PET Imaging - Checking for Beta-Amyloid Levels (in case of normal levels Alzheimer’s can be ruled out as a cause of dementia)</b></p>
  <p><b>Lumbar Puncture - Checking for Beta-Amyloid Levels and Types of Tau in CSF (Alzheimer’s as a cause of dementia can be ruled out in case of normal levels)</b></p>
  <p>Unsurprisingly, physicians can not run all tests by themselves.</p>
  <p>Expertise and proper equipment are mandatory.</p>
  <p>This is why so many receive their diagnosis for Alzheimer’s or other dementias not by a physician.</p>
  <p>After all, people should receive an accurate diagnosis.</p>
  <p>Then, treatments and follow-up care can be planned accordingly.</p>
  <p>And family members have the chance to start with sufficient financial planning.</p>
  <h2 class="">Signs of Alzheimer’s vs. Typical Age-Related Changes</h2>
  <p>The older you are the more you will forget. A phrase we all heard of. When is forgetfulness age-related and when is it due to Alzheimer’s disease? In this section, I am going to explain what is normal and what is not in the following table:</p>
  <p>Alzheimer’s related changes:</p>
  <p><b>Memory loss disrupting daily activities: </b>Alzheimer’s caused memory loss is a very common warning sign. Warning signs can include forgetting important dates, events, and information. Asking over and over for the same information is another frequent sign. Moreover, patients rely increasingly on notes as reminders. And of course, they rely more and more on family members. Especially, for things that were once handled by themselves. Now, they require help from family members. <b>Age-related changes: </b>Forgetting some appointments like going to the doctor or meeting friends. The important difference to Alzeheimer’s related changes is that individuals usually remember them later. Usually, they remember them during the day.</p>
  <p><b>Alzheimer’s Trouble to create plans and to solve problems: </b>For some patients, it might become challenging to follow a plan from A to Z. For example, they could have difficulties following an easy recipe. Their ability to work with numbers might be increasingly disrupted, too. Examples might be the tracking of monthly spending habits or counting the change at the supermarket. <b><i>Normal would be:</i></b> A typical age-related change is the occasional error when counting bills, or adding up the numbers in a card game.</p>
  <p><b>Alzheimer’s - Challenges with tasks at home, at work, or at free time activities: </b>Some people might find it difficult to finish daily tasks. Such tasks can be driving to work, going shopping in the supermarket, or taking care of the garden. Sometimes, patients diagnosed with Alzheimer’s find it hard to manage a budget or remembering the rules of a favorite game. <i><b>Normal would be:</b></i> Needing sometimes help to use the computer, play a DVD, or record a television show.</p>
  <p><b>Alzheimer’s - Difficulties with time and place:</b> Some patients get lost in a place and do not remember how they got there. Very common is to get lost in a mall, or a supermarket. Otherwise, they can lose track of the seasons, months, or dates. <i><b>Normal would be:</b></i> People can get confused about the day of the week, what time it is, or where the supermarket is situated in the city. The important difference to Alzheimer’s patients is to figure it out later during the day.</p>
  <p><b>Alzheimer’s - Confusion with understanding spatial relationships:</b> A common sign of Alzheimer’s disease is trouble with vision. Patients have difficulties judging distance, differentiating color and contrast, or reading something. As you can imagine this leads to problems with driving a car or a bicycle. <i><b>Normal would be:</b></i> People have vision changes due to glaucoma or other age-related changes like macular degeneration.</p>
  <p><i><b>Alzheimer’s - New speaking and writing difficulties:</b></i> For some people, making conversation becomes increasingly difficult. For example, they struggle to find the right words or stop in the middle of a talk. Then, they do not know how to go on with the conversation. Possible reasons are problems with finding the words or simply calling things by the wrong name. For example, they call a watch a “hand clock”. <b><i>Normal would be:</i></b> Sometimes not finding the right words.</p>
  <p><i><b>Alzheimer’s - Misplacing things and being unable to find them: </b></i>Alzheimer’s patients tend to hide things in unusual places and forget steps to find them. This happens more and more over time. Occasionally, others get even accused of stealing. <i><b>Normal would be:</b></i> The difference to Alzheimer-related misplacing of things is the frequency. Healthy old people do not often misplace things. Most importantly, they are capable of retracing steps to find them again.</p>
  <p><i><b>Alzheimer’s - Decreased or poor judgment:</b></i> Some people may experience changes in judgment or decision-making. A typical example of poor judgment is wasting money on unnecessary things (e. g. extremely overpriced kitchen equipment). <b><i>Normal would be: </i></b>Sometimes making a bad decision or judgment.</p>
  <p><i><b>Alzheimer’s - Withdrawal from social activities and work:</b></i> Some people with Alzheimer’s might isolate themselves from social activities and work. For example, they might have difficulties with remembering how to play their favorite card game. Thus, they avoid being social simply due to changes they experienced. <b><i>Normal behavior:</i></b> Feeling occasionally exhausted about social obligations, work, and family.</p>
  <p><i><b>Alzheimer’s - Personality and mood changes:</b></i> Both personality and mood can change in people suffering from Alzheimer’s. Some patients can become anxious, confused, suspicious, depressed, or fearful. Usually, they get easily upset at work or with friends. Especially, when they are out of their comfort zones. <b><i>Normal behavior:</i></b> Becoming irritable whenever a routine is disrupted.</p>
  <h2>Risk Factors for Alzheimer’s Disease</h2>
  <p>As the elderly population grows worldwide, the number of Alzheimer's patients increases rapidly. Everybody who is diagnosed with 65 or older is diagnosed with late-onset Alzheimer’s.</p>
  <p>Researchers believe that Alzheimer’s develops as a result of several factors instead of only one.</p>
  <p><b>But, there are exceptions, too.</b></p>
  <p>For example, uncommon genetic changes could increase the risk of developing it.</p>
  <p>Overall, there are multiple factors that can be considered for developing Alzheimer’s including, Genetics, Family History, Health of Heart and Blood Vessels, Education, Social and Cognitive Engagement, or Traumatic Brain Injury.</p>
  <p><i><b>The three greatest risk factors are age, genetics, and a family history of Alzheimer's.</b></i><br></p>
  <div>I would like to take the chance to list other factors that increase the risk of developing the disease. The following points are worth mentioning:</div>
  <p>- Smoking <br></p>
  <p>- Diabetes <br></p>
  <p>- Midlife obesity <br></p>
  <p>- Hypertension <br></p>
  <p>- Prehypertension (diastolic pressure from 80 to 89 mm Hg or systolic blood pressure from 120 to 139 mm Hg) <br></p>
  <p>- High Cholesterol</p>
  <p>- Late-life obesity<br></p>
  <p>- Hypertension onset after age</p>
  <h2>How to reduce the risk of developing Alzheimer’s?</h2>
  <p><b>Obviously, risk factors like age, genetics, and family history can not be changed. But, other factors like physical activity, healthy diet, or learning habits can be introduced to decrease the risk of cognitive decline and Alzheimer’s dementia.</b></p>
  <p>According to the recommendations of the World Health Organization (WHO) quitting smoking, controlling hypertension, managing diabetes, and increasing physical activities can reduce the risk of developing the disease. <br></p>
  <p>Please note the use of “decrease risk” as well as “reduce risk” of developing the disease / of cognitive decline and Alzheimer’s dementia.</p>
  <p>Both are not implying the prevention of the disease.</p>
  <p>You can still develop the disease.</p>
  <p>Even if you take all the measures to reduce the risk. Just keep in mind that there is no cure for the disease yet.</p>
  <p>Emerging evidence supports the idea that obtaining a heart-healthy diet may reduce the risk, too. <br></p>
  <p><b><i>A heart-healthy diet includes fish, chicken, nuts, vegetables, fruits, and legumes while limiting sugar, red meat, and saturated fats.</i></b></p>
  <p>Another interesting factor is education. For instance, people who underwent formal education for multiple years have a lower Alzheimer’s risk compared to people with fewer years.</p>
  <p>The same accounts for other dementias, too.</p>
  <p>A possible explanation might be that having more years of education creates a<b> “cognitive reserve”.</b></p>
  <p><i><b>A cognitive reserve is known as the brain’s ability to efficiently use its cognitive networks.</b></i></p>
  <p>The networks consist of neuron-to-neuron connections and assist the individual to perform cognitive tasks despite changes in the brain.<br></p>
  <p>Such changes include the accumulation of beta-amyloid and tau.</p>
  <p>Next to the years of education, having a mentally stimulating job or engaging in mentally stimulating activities (e. g. playing chess) may also help to build the cognitive reserve.</p>
  <p>Nonetheless, the underlying reason for the relationship between formal education and reduced Alzheimer’s risk is still not clear.</p>
  <p>Last but not least, social engagement is another factor.</p>
  <p><i><b>There are studies proposing that being socially active throughout life may support brain health.</b></i></p>
  <p>It might even decrease Alzheimer’s risk.</p>
  <p>In addition, being socially active might help individuals to build their cognitive reserve. But, it is still not clear how this may occur. <br></p>
  <p>Finally, cognitive engagement rounds up the list.</p>
  <p>Multiple studies suggest that being mentally active may support brain health, too.</p>
  <p>Remaining mentally active might also reduce the risk of developing Alzheimer’s and other dementias.<br></p>
  <p>And just like being socially active, being mentally active might help to build cognitive reserve.</p>
  <p>Though, how this may occur is not yet known.</p>
  <p>Further research is required to understand what indeed helps to reduce the risk.</p>
  <p>For example, researchers do still not know which types of exercises and how often a person has to exercise to reduce dementia risk.</p>
  <p>To summarize, you learned about different ways that might help you to reduce your Alzheimer’s risk.</p>
  <p>But, you also know that reducing the risk does not prevent the disease.</p>
  <p>You can still develop it anyway.</p>
  <p>Work on your education, cognitive and social engagement, and of course eat healthily.</p>
