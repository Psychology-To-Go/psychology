  <h2>Treatment of Alzheimer’s Dementia</h2>
  <p>As discussed in the Diagnosis Section, individuals have to receive an accurate diagnosis in order to start the right treatment. There are several ways to treat Alzheimer’s dementia including Pharmacologic Treatment, and Non-Pharmacologic Therapy.</p>
  <h2>Pharmacologic Treatment</h2>
  <p><b>To this date, there is no medication (pharmacologic treatment) that stops the damage and destruction of neurons caused by Alzheimer’s disease. This also accounts for the symptoms that make the disease fatal.</b></p>
  <p>It can not be stopped by currently available medication.</p>
  <p>Nonetheless, the U.S. FDA (Food and Drug Administration) gave its approval to five drugs as Alzheimer’s treatment. The five drugs consist of:</p>
  <p><b>1. Donepezil,</b></p>
  <p><b>2. Galantamine,</b></p>
  <p><b>3. Memantine,</b></p>
  <p><b>4. Memantine in combination with Donepezil,</b></p>
  <p><b>5. Rivastigmine.</b></p>
  <p>Except for memantine, all of the above lead to a temporary improvement of the cognitive symptoms. This happens due to the increase of neurotransmitters in the brain.</p>
  <p>Memantine is the exception because it blocks specific brain receptors from excess stimulation.</p>
  <p>This is crucial because excess stimulation can cause nerve cell damage.</p>
  <p>Unsurprisingly, the drugs’ effectiveness varies from individual to individual.</p>
  <p>So far, there is no approval by the FDA for a drug, which treats behavioral and psychiatric symptoms. <br></p>
  <p>These symptoms may arise in moderate and severe Alzheimer’s dementia.</p>
  <p>But, patients may receive drugs prescribed by a physician in order to treat symptoms like hallucinations.</p>
  <p>For example, antipsychotics may be effective for some individuals.</p>
  <p>Next to hallucinations, aggression and agitation may be treated with it, too.</p>
  <p>Antipsychotics must be considered with extreme caution, before making the decision to use them. There is an increased risk of strokes and death for those suffering from dementia.<br></p>
  <p>Manufacturers are obliged to label such drugs with a “black box warning” about their risks. <br></p>
  <p>And the warning additionally states that the medication is not approved by the FDA to treat dementia symptoms.</p>
  <p>As you can imagine, many factors play a role to develop effective Alzheimer’s treatment. <br></p>
  <p>Just to name a few, which contribute to the difficulty of effective treatment:</p>
  <p><b>- Recruiting enough patients for a clinical study takes very long.</b></p>
  <p><b>- Recruitment of sufficiently diverse participants is difficult.</b></p>
  <p><b>- Limited knowledge about the biological processes in the brain.</b></p>
  <p><b>- Controlling how the investigational treatment affects the disease progression.</b></p>
  <p><b>- Possibility of results due to pure chance.</b></p>
  <p>There are several other factors that contribute to the difficulty of developing a new treatment.</p>
  <p>But, I think you get the idea.</p>
  <p>It is simply not that easy to come up with a drug that helps everybody.</p>
  <h2>Non-Pharmacologic Therapy</h2>
  <p><b>Non-pharmacologic therapies are often used for individuals with Alzheimer’s dementia, who want to maintain or improve their cognitive function. Other aspects can be improved as well. For example, the overall quality of life can be improved, too.</b></p>
  <p>As the name of the therapy already hints, they are without medication.</p>
  <p>Some individuals use non-pharmacologic therapies to reduce behavioral symptoms. <br></p>
  <p>Such symptoms include<i><b> agitation, aggression, apathy, depression, sleep disturbances, and wandering.</b></i></p>
  It is noteworthy, non-pharmacologic therapies do not stop the neuronal damage, which ultimately leads to Alzheimer’s symptoms.
  <p>Nonetheless, let’s find out how the non-pharmacologic therapies look like. Some examples are:</p>
  <p><b>- Cognitive therapy approaches</b>: This type of therapy is targeted at individuals with mild to moderate Alzheimer’s. Exercises may include arithmetic problems, remembering numbers, word puzzles, and spatial orientation. Usually, it is a one-on-one or group session, which happens one or two times per week. The session takes around 30 to 90 minutes time.</p>
  <p><b>- Physical, emotional, and social stimulation:</b> Interventions may include different activities like preparing a meal together, writing letters, group discussions on different topics, listening to music, making art, and physical activities. Individuals participate two to three times per week. Sessions last around 30 to 60 minutes depending on the activities.</p>
  <p><b>- Validation therapy:</b> This therapy is an emotion-oriented treatment. It utilizes special communication techniques in order to create an atmosphere of comfort and care for the person suffering from Alzheimer’s. Caregivers, psychologists, and family members use this approach to make the individual feel understood and comfortable. One should not be judged by behavior at any time. For example, a man who has Alzheimer’s thinks that his wallet has been stolen. He is furious and wants to go to the police. By using the validation approach one is understanding and gives a solution like searching for it together. And in case it would not show up to then go to the police station. It is important to not question the behavior or that the wallet is stolen. The focus is on the patient and nobody else.</p>
  <p><b>- Reminiscence therapy:</b> This is another type of emotion-oriented treatment. Reminiscence derives from the Latin word “reminiscor” meaning “I remember”. In this therapy, patients are encouraged to talk about their past. For example, about their school time. The goal is to improve one’s mental abilities. Another aim is to enhance the quality of life and decrease the psychological effects that can be caused by Alzheimer’s. The sessions happen either in an individual or group setting. The session time is very flexible. A good landmark is between 20 minutes and two hours.</p>
  <p><b>- Occupational therapy:</b> This therapy involves practicing everyday activities like brushing one’s teeth.</p>
  <p><b>- Psychological treatment: </b>It may be considered if the patient starts to show symptoms of severe mental health issues.</p>
  <p>There are a large number of other non-drug interventions available to treat Alzheimer’s.</p>
  <p>All that is left to do is choosing one or two.</p>
  <p>It is just important to keep track of what works and what works not.</p>
  <h2>Improving Quality of Life</h2>
  <p>There are several studies, which indicate how affected individuals and their caregivers can be positively influenced by active management of Alzheimer's.</p>
  <p>These same study results also account for other dementias. <br></p>
  <p>Active management of Alzheimer’s can look from patient to patient differently.</p>
  <p>Nonetheless, here are some general ideas how active management can look like:</p>
  <p>- Checking for available treatment options and choosing a relevant one.</p>
  <p><b>- Visiting support groups related to dementia: </b>gives the opportunity to connect with other individuals and gain valuable insights. Especially, from those who are further ahead in the progression (e. g. what new problems occur, what treatment plan to use when, which specialists to visit, etc.)</p>
  <p><b>- Educating family and friends about the disease.</b> This is important for them to prepare for possible behavior and personality changes in the future.</p>
  <p><b>- Going to caregiver training with family members</b>. Especially, if an external health care provider is not an option due to finances.</p>
  <p><b>- Planning for the future.</b> Not only financially, but also in terms of organizing care among physicians, caregivers, and other health care providers.</p>
  <p>- Taking part in activities that make the patient happy.</p>
  <p><b>- Giving meaning and purpose to the life of the patient</b>. The feeling of being valued and not a burden is crucial.</p>
  <p>The point concerned with purpose in life is the most vital one for me.</p>
  <p>Just think about it, everybody craves some sort of meaning in life. For example, family members should listen to the patient’s biggest mistakes and successes in life.</p>
  <p>This way the patient feels valued and acknowledged.</p>
  <p>Of course, there are other ways to achieve the same effect.</p>
  <p>Find out what makes the affected individual happy.</p>
  <p>Sometimes just asking the question “What would make you happy?” does the job.</p>
  <h2>Prevalence of Alzheimer’s Disease</h2>
  <p>Millions have Alzheimer’s or other dementias. And as the size of the global population age 60 and older increases persistently, the patients with Alzheimer’s or other dementias will grow, too.</p>
  <p>The prevalence statistics vary depending on where you are living.</p>
  <p>Nonetheless, the number that I came often across was that:</p>
  <p>Every second person above 90 is diagnosed with Alzheimer’s or other dementias.</p>
  <p>According to the World Health Organization (WHO), around 50 million people are diagnosed with dementia. Nearly 10 million new cases come up every year.<br></p>
  <p>Every 3.15 seconds, somebody in the world develops Alzheimer’s or other dementias. The number of people living with dementia worldwide in 2030 will reach 75.63 million and 135.46 million in 2050.<br></p>
